# Colorful terminal output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
MAGENTA='\033[0;35m'
CYAN='\033[0;36m'
NC='\033[0m' # No Color
BOLD='\033[1m'
UNDERLINE='\033[4m'
RESET='\033[0m'
INFO="${BLUE}[INFO]${NC}"
SUCCESS="${GREEN}[SUCCESS]${NC}"
WARNING="${YELLOW}[WARNING]${NC}"
ERROR="${RED}[ERROR]${NC}"
DRYRUN="${MAGENTA}[DRY-RUN]${NC}"
RUNNING="${CYAN}[RUNNING]${NC}"
BOLD_TEXT="${BOLD}BOLD${RESET}"
UNDERLINE_TEXT="${UNDERLINE}UNDERLINE${RESET}"